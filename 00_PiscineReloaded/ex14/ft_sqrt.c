int	ft_sqrt(int nb)
{
	int	i;
	int	sqrt;

	sqrt = 0;
	i = 1;
	while (nb > 0)
	{
		nb -= i;
		i += 2;
		sqrt++;
	}
	if (nb == 0)
		return (sqrt);
	else
		return (0);
}
