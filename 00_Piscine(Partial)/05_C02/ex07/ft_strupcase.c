char	*ft_strupcase(char *str)
{
	char	*copy;

	copy = str; 
	while (*str != '\0')
	{
		if ((*str >= 'a' && *str <= 'z'))
		{
			*str = *str - 32;
			str++;
		}
		else
		{
			str++;
		}
	}
	*str = '\0';
	return (copy);
}
