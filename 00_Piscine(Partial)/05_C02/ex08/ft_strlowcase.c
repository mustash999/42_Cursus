char	*ft_strlowcase(char *str)
{
	char	*copy;

	copy = str; 
	while (*str != '\0')
	{
		if ((*str >= 'A' && *str <= 'Z'))
		{
			*str = *str + 32;
			str++;
		}
		else
		{
			str++;
		}
	}
	*str = '\0';
	return (copy);
}
