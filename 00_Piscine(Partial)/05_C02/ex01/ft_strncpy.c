char	*ft_strncpy(char *dest, const char *src, unsigned int n)
{
	char	*original_pointer;

	original_pointer = dest;
	while (*src != '\0' && n > 0)
	{
		*dest = *src;
		dest++;
		src++;
		n--;
	}
	while (n > 0)
	{
		*dest = '\0';
		dest++;
		n--;
	}
	return (original_pointer);
}
